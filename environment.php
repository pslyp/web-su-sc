							<h3 align="center" class ="pt-5"><a>สภาพแวดล้อมในมหาวิทยาลัย</a></h3>
							<div class ="pt-4 event-readmoree" >
							<p >&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp เมื่อคืนวานฉันยังนั่งเตรียมของจะมาอยู่หอจนดึก มันตื่นเต้นจนบอกไม่ถูก นี่เป็นครั้งแรกที่ฉันจะต้องมาอยู่ ไกลบ้านนานๆ ต้องไปอยู่กับใครที่ไม่รู้จัก ต้องเริ่มสังคมใหม่ใช้ชีวิตด้วยตัวเองเพียงลำพัง เมื่อฉันสอบติดคณะ วิทยาศาสตร์ มหาวิทยาลัยศิลปากรคนแถวบ้านถามฉันว่าทำไมต้องไปอยู่หอ   ในเมื่อบ้านก็อยู่แค่นี้(แถวจรัญสนิทวงศ์) ฉันตอบว่าฉันสอบติดคณะวิทยาศาสตร์ อยู่ที่วิทยาเขตพระราชวังสนามจันทร์  คนพวกนั้นต่างทำหน้างงๆ  ซึ่งก็คงเหมือนฉันในตอนแรกที่ได้รู้จักคณะนี้จากอาจารย์แนะแนวที่โรงเรียนเก่า</p>
							<p >&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp นั่งรถปรับอากาศประจำทางมาชั่วโมงกว่าๆ จากสถานีขนส่งสายใต้ ข้าวของพะรุงพะรัง ก้าวแรกที่ก้าวผ่านรั้ว มหาวิทยาลัยศิลปากร ความรู้สึกแรกคือ แน่ใจนะว่านี่ คือที่ ๆ ฉันจะมาเรียน  มาเอาใบปริญญาเหมือนมารีสอร์ต ฉันขนของมาเพื่อพักผ่อน ผู้คนปั่นจักรยาน แต่งตัวตามสบาย กางเกเลหลากสี ที่ติดกับต้นไม้ใบเขียวด้านซ้าย เป็นสนามฟุตบอล  ด้านขวาเป็นสวนหย่อมติดริมสระน้ำ ลุงยามยิ้มให้อย่างเป็นมิตรกล่าวต้อนรับเปิดเทอม อย่างเบิกบาน ฉันเดินเลาะสวนหย่อมไปเรื่อย ๆ กริ๊ง ๆ "น้องไปกับพี่ไหมคะ อยู่หอไหนพี่จะไปส่ง"   ฉันลังเลเล็กน้อยก่อนบอกว่า "อยู่หอเพชรรัตน์ 4" แล้วขึ้นจักรยานพี่คนนั้นไป  ฉันถึงหอพักแล้ว จัดข้าวของแล้ว แต่เพื่อนร่วมห้องยังไม่มา  ฉันเตรียมผ้าถุงมาเพื่อใช้ผลัดอาบน้ำ เพราะคิดว่า ห้องน้ำรวมคงหมายถึง มีอ่างใส่น้ำใหญ่ ๆ แล้วล้อมวงอาบ แต่กับสบายกว่าที่คิดเยอะ ผ้าถุงผืนนั้นเลยนอนแห้งคาตู้ จนกระทั่งจบปี 4 </p>
							<p>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp วันรุ่งขึ้นฉันไปซื้อจักรยานที่ตลาด ฉันปั่นสำรวจมหาวิทยาลัยตากแดดจนตัวเกรียม จำได้ว่าวันแรกไปตั้งต้น ที่ป้อมยามหน้ามหาวิทยาลัย ด้านถนนราชมรรคาตรงไปยังสี่แยกตึก 50 ปี ผ่านคณะอักษรศาสตร์ หน้าคณะมีศาลเจ้าที่ของมหาวิทยาลัย ตรงข้ามเป็นสระแก้ว ปั่นจักรยาน
							<p>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ตรงไปเรื่อย ๆ ผ่านโรงอาหารสระแก้ว หรือที่นักศึกษาเรียกติดปากว่า "ยูเนี่ยน" ข้างบนเป็นที่ทำการสโมสรนักศึกษาและชมรมต่าง ๆ ถัดมาอีกตึกเป็นศูนย์เรียนรวม 2 เป็นชื่อที่ฝ่ายโครงงานและสถานที่เรียก หอประชุม เป็นชื่อที่อยู่ในตารางสอนและตึกกระทะเป็นชื่อที่พวกเราเรียกกัน </p>
							<p>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ด้านหลังของตึกกระทะเป็นส่วนของคณะเภสัชศาสตร์ ปั่นจักรยานตรงไปอีกหน่อยเป็นศูนย์คอมพิวเตอร์ และหอสมุดวิทยาเขต	พระราชวังสนามจันทร์ ซึ่งตอนนี้ได้มีอาคารหม่อมหลวงปิ่น มาลากุล สร้างเพิ่มขึ้นมา ภายในอาคารจัดเป็นที่บริการคอมพิวเตอร์ หอสมุด สื่อวิดีทัศน์ต่างๆ และเป็นหอจดหมายเหตุของมหาวิทยาลัยศิลปากร ด้านหลังมีบ่อบำบัดน้ำเสีย </p>
							<p>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ถัดไปเป็นคณะวิศวกรรมศาสตร์และเทคโนโลยีอุตสาหกรรม ฝั่งตรงข้ามของหอสมุด และศูนย์คอมพิวเตอร์  เป็นคณะวิทยาศาสตร์ ซึ่งฉันจะต้องมาเรียนในอีกไม่ช้า ฉันแวะเข้าไปสำรวจ มีอาคาร ว.1 - ว.4 ซึ่งขณะนั้นกำลัง ก่อสร้างอาคารเรียนรวมวิทยาศาสตร์ หรือ "ร.ว.ท." อาคารเก็บสารเคมี และอาคารปฏิบัติการฟิสิกส์ ใหญ่โตจริง แต่เงียบเหงามาก คงเป็นเพราะยังไม่เปิดเทอม ฉันปั่นจักรยานต่อไปตามถนน ใกล้จะเที่ยงเต็มทีท้องเริ่มร้อง ฉันปั่นจักรยานเลาะไปหลังหอเพชรรัตน์ 4 ซึ่งขนานกับหอเพชรรัตน์ 3 ตรงไปยังร้านอาหารเพชรรัตน์ หรือที่เรียกกันติดปากว่า เพชรช๊อบ เพื่อนยามยากของเด็กหอ บรรยากาศดี ริมบึงลานนมร่มรื่น (ลานโล่งที่มีร้านขายนมแสนอร่อย) </p>
							<p>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ในระแวกนั้นนอกจากมีหอเพชรรัตน์ 1 - 6 ซึ่งเป็นหอพักหญิงแล้ว ยังมีโรงเก็บเครื่องมือ และบ้านจักรยาน ร้านซ่อมจักรยานที่ช้าที่สุดในประเทศไทย แต่เป็นอู่ที่ทันสมัยที่สุดแล้วสำหรับจักรยานในมหาวิทยาลัย  เมื่ออิ่มท้องแล้วสองขาก็ปั่นจักรยานต่อไปตามทางเดิมเมื่อกี้ เลี้ยวขวาเพื่อเข้ามาทางเพชนรัตน์ 1 - 6 แต่ถ้าเลี้ยวซ้ายจะเป็นหอทับแก้ว 3 หอพักชายที่ไม่เคยหลับไหล บริเวณนี้มีโรงยิม สนามเปตอง สนามบาส สนามฟุตบอล คอร์ดเทนนิสหรือที่เรียกรวม ๆ ว่า Sport Complex ศูนย์รวมนักศึกษายามที่ไม่มีที่ไป เหงาพวกเราก็ไปออกกำลังกายกัน ปั่นจักรยานจนสุดรั้วมหาวิทยาลัยถ้าเลี้ยวซ้ายจะเป็นอาคารศิลป์พีระศรี 1,2 และ 3  ซึ่งเป็นที่เรียนของคณะสถาปัตยกรรมศาสตร์ ปี1 และคณะจิตรกรรมชั้นปีสูง ๆ ถ้าเลี้ยวขวาจะเป็นโรงพิมพ์มหาวิทยาลัยถัดไปหน่อยเป็นบ้านพัก บุคลากรในมหาวิทยาลัย ตึกขั้นและโรงงานของคณะมัณฑณศิลป์ ปั่นจักรยานต่อไป ตามทางหลังหอเพชรรัตน์ 1,2 และ 6 เลาะไปยังสระแก้ว ระหว่างทางมีบ้านไทยสมัย ร.6 ทรุดโทรม</p>
							<p>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ดูวังเวง ผ่านหลังคณะวิทยาศาสตร์ ผ่านบ้านจักรยาน ข้ามสะพานไปตรงเนินวิดยา ผ่านเกาะนกไปยังหอทับแก้ว 2 ซึ่งเป็นหอชายและหอทับแก้ว 1 หอหญิงที่อยู่ติดกัน เลาะรอบสระแก้วไปเรื่อยๆ ด้านซ้ายมือเป็นคณะศึกษาศาสตร์ ตึกอธิการบดี และกองกิจการนักศึกษา ที่บริการข้อมูลข่าวสารต่างๆ ตรงนั้นมีห้องพยาบาลแอบอยู่ด้วย ด้านขวามือตรงข้ามตึกอธิการบดีเป็นอาคารวัชรนาฎยสภา หรือ A4 ซึ่งเป็นอาคารที่ใช้จัดแสดงต่างๆ เช่น ดนตรี ละครเวที เป็นต้น </p>
							<p>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ถัดไปนิดหนึ่งคือสะพานข้ามสระแก้ว คุณเชื่อไหมที่ตรงนั้นทำให้ลืมได้ทุกอย่างเหมือนหลุดมาอยู่อีกโลกหนึ่งที่นี่แหละคือสวรรค์กลางมหาวิทยาลัย ปั่นจักรยานเลยไปอีกนิดจะเป็น 3 แยก ตรงกลางมีวงเวียนที่มีปฏิมากรรมสีแดงตั้งอยู่ ด้านตรงข้ามเป็นร้านอาหารสระแก้วบรรยากาศดี ข้าง ๆ มีสนามเปตอง จากวงเวียนถ้าเลี้ยวซ้ายแล้วตรงไปจะผ่านสนามแบดมินตันและตระกร้อ บ้านพักอาจารย์ เลยไปอีกนิด จะเป็นด้านหน้าของคณะศึกษาศาสตร์ ด้านตรงข้ามคณะศึกษาศาสตร์เป็นโรงเรียนสาธิตแห่งมหาวิทยาลัยศิลปากร มีสระว่ายน้ำขนาดมาตรฐาน และสนามกีฬาอีกหลายชนิดที่นักศึกษาเข้าไปใช้ได้และสุดทางเป็นประตูออก ไปสู่พระราชวังสนามจันทร์  กลับมาที่วงเวียนถ้าเลี้ยวขวาตรงไปยังสี่แยกหน้าตึก 50 ปี ด้านซ้ายตรงไปประตูออกสู่ถนนราชมรรคา ตรงไปเป็นบ้านพักอาจารย์มีถนนเชื่อมต่อไปยังคณะเภสัชศาสตร์ </p>
	                        <p>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ถ้าเลี้ยวขวาก็จะกลับไปที่หน้ายูเนี่ยนเช่นเดิม ตรงข้ามยูเนี่ยนมีศาลา 8 เหลี่ยม สมัย ร.6 ซึ่งพวกเราเรียกว่า ศาลาสระแก้ว ร่มรื่นและเย็นสบายใต้ร่มไม้ มีลมพัดเอาไอน้ำจากสระแก้วขึ้นมาให้ชุ่มช่ำหัวใจ สี่โมงเย็นแล้วฉันปั่น จักรยานไปยัง 7-eleven ซึ่งเป็นศูนย์รวมความบันเทิงในมหาวิทยาลัย ซึ่งเดี๋ยวนี้อยู่บริเวณใต้อาคารหม่อมหลวงปิ่นมาลากุล นอกจากนี้ยังมีร้านอาหารริมบึง ตั้งอยู่ตรงนั้นด้วย เหนื่อยแสนเหนื่อยที่ปั่นจักรยานวนไปวนมาในมหาวิทยาลัย เนื่องจากถนนเชื่อมถึงกันหมด ต้นไม้ใหญ่ร่มครึม นกน้อยส่งเสียงร้อง กระรอกกระโดดข้ามไปมาบนต้นไม้ และสัตว์ประจำมหาวิทยาลัย ตัวเงินตัวทองก็เดินมาบนถนนให้อวดโฉมให้เห็น กว่าฉันจะลากสังขารอันหนักอึ้งไปบนหอชั้น 3 ได้ ทรมานแทบตาย แต่ก็เป็นเหตุการณ์ที่จำไม่ลืม แม้วันนี้มหาวิทยาลัยจะเปลี่ยนไปบ้าง ต้นไม้น้อยลงรถเพิ่มขึ้น และถนนที่กว้างกว่าเดิมในบางช่วงก็ไม่ได้ทำให้ฉันรักมหาวิทยาลัยน้อยลง และวันนี้ฉันปั่นจักรยานคันเก่าไปรับน้องใหม่ที่หน้ามหาวิทยาลัย ความรู้สึกของน้องคงไม่ต่างจากฉันในวันนั้นฉันสั่นกระดิ่งกริ๊งๆ "น้องไปกับพี่ไหมคะ อยู่หอไหนพี่จะไปส่ง"</p>	
						
							
