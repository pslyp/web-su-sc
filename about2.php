<br>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ต่อมาในปีการศึกษา 2535 - 2538 คณะวิทยาศาสตร์พิจารณารับนักศึกษาชั้นปีที่ 1 จาก 20 คน เป็น 260 คน ถึงแม้ว่าคณะวิทยาศาสตร์จะพิจารณารับนักศึกษาเพิ่มขึ้นเกือบทุกปีแล้วก็ตามเมื่อมีการวิเคราะห์ จำนวนความต้องการนักวิทยาศาสตร์ทั่วประเทศแล้ว พบว่าประเทศไทยยังขาดแคลนนักวิทยาศาสตร์ อีกถึงปีละ 10,000 คน ทบวงมหาวิทยาลัยจึงขอความร่วมมือจากคณะวิทยาศาสตร์ทุกมหาวิทยาลัยของรัฐ ทั่วประเทศ	ให้พิจารณาผลิตบัณฑิตทางวิทยาศาสตร์เพิ่มให้เพียงพอต่อความต้องการของประเทศ ฉะนั้นคณะวิทยาศาสตร์จึงจำเป็นต้องรับนักศึกษาชั้นปีที่ 1 เพิ่มขึ้นตั้งแต่ปีการศึกษา 2539 อีก 180 คนและเนื่องจากสาขาวิชาคณิตศาสตร์เป็นอีกสาขาวิชาหนึ่งที่ขาดแคลนอย่างยิ่ง ซึ่งทางทบวงมหาวิทยาลัย ขอให้รับเพิ่มอีกปีละ10 คน ดังนั้นในปีการศึกษา 2539 คณะวิทยาศาสตร์จึงรับนักศึกษาชั้นปีที่ 1 เพิ่มอีก 190 คนรวมจากเดิม 260 คนเป็น450คน ต่อมาสาขาวิชาคณิตศาสตร์พิจารณาเพิ่มอีก 10 คน ฉะนั้นในปีการศึกษา 2540 คณะวิทยาศาสตร์รับนักศึกษาชั้นปีที่ 1 รวมทั้งสิ้น 460 คน<br>
