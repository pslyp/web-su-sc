					<p href="#" class="event-readmoree">&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp คณะวิทยาศาสตร์ได้ก่อตั้งขึ้น ในวันจันทร์ที่ 27 ธันวาคม ปีพ.ศ. 2514 เป็นคณะที่ 7 ของ มหาวิทยาลัยศิลปากร โดยเพื่อให้สอดคล้องกับนโยบายการพัฒนาประเทศที่เกี่ยวข้องกับงานการศึกษาทางด้านวิทยาศาสตร์แต่เนื่องจากในปีการศึกษา 2515 ซึ่งเป็นปีการศึกษาแรก อาคารเรียนของคณะวิทยาศาสตร์ ณ วิทยาเขตพระราชวังสนามจันทร์ จังหวัดนครปฐมยังสร้างไม่เสร็จ จึงจำเป็นต้องฝากนักศึกษารุ่นแรกเรียนที่แผนกพฤกษศาสตร์ คณะวิทยาศาสตร์ จุฬาลงกรณ์มหาวิทยาลัย เป็นการชั่วคราวเป็นเวลา 1 ปี จนในปีการศึกษา 2516 จึงได้ย้ายมาดำเนินการต่อที่อาคารหลังแรกของคณะวิทยาศาสตร์ และได้จัดสร้างอาคารประกอบอื่น ๆ เช่น โรงเลี้ยงสัตว์ทดลองและเรือนต้นไม้ของภาควิชาชีววิทยา<br>
<br>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp คณะวิทยาศาสตร์เปิดสอนหลักสูตรวิทยาศาสตรบัณฑิตแก่นักศึกษารุ่นแรก จำนวน 48 คน แบ่งเป็น 3 สาขาใน 2 ภาควิชาคือ สาขาวิชาคณิตศาสตร์ และ สาขาวิชาสถิติใน ภาควิชาคณิตศาสตร์ และ สาขาวิชาชีววิทยา ใน ภาควิชาชีววิทยาจากนั้นในปีการศึกษา 2519 คณะวิทยาศาสตร์ได้เปิดสอนหลักสูตรวิทยาศาสตรบัณฑิต สาขาวิชาเคมี และ สาขาวิชาฟิสิกส์ เพิ่มอีก 2 สาขาวิชาเอก ทางคณะวิทยาศาสตร์มีนโยบายการพัฒนาการศึกษาในด้านคุณภาพการศึกษาและการจัดตั้งสาขาวิชาใหม่ๆอยู่เสมอ ซึ่งมีผลกระทบกับจำนวนนักศึกษาที่ต้องเพิ่มมากขึ้นด้วย ดังนั้นตั้งแต่ปีการศึกษา 2520 เป็นต้นมาคณะวิทยาศาสตร์จึงรับนักศึกษาชั้นปีที่ 1 เพิ่มขึ้นทุกปี<br><br>
