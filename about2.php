<br>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ในปีงบประมาณ 2525 คณะวิทยาศาสตร์ได้รับงบประมาณเพื่อก่อสร้างโรงเลี้ยงแมลงเพื่องานวิจัยของภาควิชาชีววิทยาอีก 1 หลัง โดยสร้างต่อเชื่อมกับเรือนกระจกสำหรับปลูกพืชทดลอง. หลังจากเปิดหลักสูตรวิทยาศาสตร์ทั่วไปเป็นเวลาพอสมควรคณะวิทยาศาสตร์ ประสบปัญหาการจัดการเรียนการสอนเป็นอย่างมาก จึงมีดำริที่จะหยุดผลิตบัณฑิตสาขาวิชานี้ แต่เล็งเห็นถึงความสำคัญของ สาขาวิชาวิทยาศาสตร์สิ่งแวดล้อมดังนั้นนับแต่ปีการศึกษา 2528 เป็นต้นมาคณะวิทยาศาสตร์จึงรับนักศึกษาเข้าศึกษาหลักสูตรวิทยาศาสตร์ทั่วไปเพียง สาขาวิชาวิทยาศาสตร์สิ่งแวดล้อม เพียงสาขาเดียว <br> 
<br>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp ต่อมาปีการศึกษา 2532 คณะวิทยาศาสตร์ได้รับการอนุมัติ ภาควิชาวิทยาศาสตร์สิ่งแวดล้อม จากทบวงมหาวิทยาลัยซึ่ง คณะวิทยาศาสตร์ มหาวิทยาลัยศิลปากรเป็นคณะวิชาแรกในประเทศไทย ที่เปิดสอนหลักสูตรวิทยาศาสตร์สิ่งแวดล้อมในระดับปริญญาตรี การเรียนการสอนในอีกสาขาหนึ่งที่มีการพัฒนาอย่างต่อเนื่องในคณะวิทยาศาสตร์ ควบคู่ไปกับการเติบโตของศูนย์คอมพิวเตอร์ของมหาวิทยาลัยคือ สาขาวิทยาการจัดการคอมพิวเตอร์ ซึ่งได้รับความเห็นชอบผ่านการอนุมัติจากทบวงมหาวิทยาลัยในปี พ.ศ. 2531<br>

