<br>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp เนื่องจากคณะวิทยาศาสตร์ต้องรับภาระสอนวิชาพื้นฐานทางวิทยาศาสตร์ให้กับคณะวิชาต่าง ๆ รวมถึงคณะเภสัชศาสตร์และเทคโนโลยีอุตสาหกรรม จึงจำเป็นต้องขยายพื้นที่อาคารวิทยาศาสตร์ ให้เพียงพอกับจำนวนนักศึกษาที่เพิ่มขึ้น <br>
<br>&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp คณะวิทยาศาสตร์จึงของบประมาณสร้างอาคารเพิ่มเติมเพื่อเป็นห้องปฏิบัติวิชาพื้นฐานทั้งหมด ซึ่งในระยะนี้มหาวิทยาลัยมีนโยบายจัดตั้งศูนย์เครื่องมือวิจัยวิทยาศาสตร์และเทคโนโลยีด้วย แต่เนื่องจากโครงการจัดตั้งศูนย์เครื่องมือวิจัยวิทยาศาสตร์และเทคโนโลยียังไม่ได้รับการอนุมัติ อย่างเป็นทางการจึงไม่ได้รับงบประมาณก่อสร้าง โครงการมหาวิทยาลัยจึงให้ที่ทำการ	ศูนย์เครื่องมือวิจัยวิทยาศาสตร์และเทคโนโลยีอยู่ในความดูแลของคณะวิทยาศาสตร์และให้ใช้อาคารวิทยาศาสตร์หลังที่ 4 ส่วนหนึ่งเป็นที่ทำการของศูนย์ด้วย อาคารหลังนี้จึงเรียกชื่อว่า อาคารปฏิบัติการรวมและศูนย์เครื่องมือวิทยาศาสตร์(อาคารวิทยาศาสตร์ 4)<br>
